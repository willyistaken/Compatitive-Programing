#pragma GCC optimize("Ofast")
#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
typedef unsigned long long ull;
int main(){
	ios_base::sync_with_stdio(0);cin.tie(0);cout.tie(0);
	ull n;
	while(true){
		cin>>n;
		assert(n!=ULONG_LONG_MAX);
		if(n==0) break;
	}
	cout<<0<<"\n";
	return 0;
}
